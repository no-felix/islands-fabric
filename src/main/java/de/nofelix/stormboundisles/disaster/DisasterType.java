package de.nofelix.stormboundisles.disaster;

/**
 * Enumerates the different types of disasters that can occur.
 */
public enum DisasterType {
	/** A meteor shower disaster. */
	METEOR,
	/** A blizzard disaster. */
	BLIZZARD,
	/** A sandstorm disaster. */
	SANDSTORM,
	/** A toxic spore cloud disaster. */
	SPORE,
	/** A levitating crystal storm disaster. */
	CRYSTAL_STORM
}