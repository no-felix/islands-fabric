package de.nofelix.stormboundisles.disaster;

/**
 * Enumerates the different types of disasters that can occur.
 */
public enum DisasterType {
	/** A meteor shower disaster causing direct damage. */
	METEOR,
	/** A blizzard disaster causing freezing effects. */
	BLIZZARD,
	/** A sandstorm disaster causing blindness. */
	SANDSTORM,
	/** A toxic spore cloud disaster causing poison. */
	SPORE,
	/** A levitating crystal storm disaster causing levitation. */
	CRYSTAL_STORM
}